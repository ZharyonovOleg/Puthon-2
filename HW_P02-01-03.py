def dct_2(list_2): # O(N LOG N)
    dct3 = {}
    list_3 = dict(list_2)
    for a in range(3):
        indexmax = max(list_3.items(), key=lambda k_v: k_v[1])
        del list_3[indexmax[0]]
        dct3[indexmax[0]] = indexmax[1]
    return dct3



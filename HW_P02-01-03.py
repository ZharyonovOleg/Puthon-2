dct = {'Рога': 156000, 'Копыта': 1236888, 'Ромашка': 1564568,
       'Victory': 99999999999, 'Poor': 1, 'Zero': 0}

# 1
print('Решение первым методом: ')

def dct_1(dct1): # O(N**2)
    for a in range(len(dct1)):
        indexmin = a
        for b in range(a + 1, len(dct1)):
            if dct1[b][1] > dct1[indexmin][1]:
                indexmin = b
        dct1[a], dct1[indexmin] = dct1[indexmin], dct1[a]
    return dct1[0:3]


list1 = list(dct.items())

for a in dct_1(list1):
    print(a[0], ':', a[1])

